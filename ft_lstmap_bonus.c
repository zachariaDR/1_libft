#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*node;
	void	*edit;

	new_lst = NULL;
	if (!lst || !f || !del)
		return (new_lst);
	while (lst)
	{
		edit = f(lst->content);
		node = ft_lstnew(edit);
		if (!node)
		{
			del(edit);
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, node);
		lst = lst->next;
	}
	return (new_lst);
}
